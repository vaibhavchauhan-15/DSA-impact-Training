// C++ program to demonstrate the use of if-else statement
// to check if a person is an adult or a minor

#include <iostream>
using namespace std;

int main() {
    int age = 20;
    if (age >= 18) {
        cout << "You are an adult." << endl;
    } else {
        cout << "You are a minor." << endl;
    }
    
    return 0;
} 